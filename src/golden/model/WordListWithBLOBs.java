package golden.model;

public class WordListWithBLOBs extends WordList {
    private String indonesian;

    private String audio;

    private String chinese;

    private String nature;

    private String antonym;

    private String homoionym;

    private String usedPhrases;

    public String getIndonesian() {
        return indonesian;
    }

    public void setIndonesian(String indonesian) {
        this.indonesian = indonesian;
    }

    public String getAudio() {
        return audio;
    }

    public void setAudio(String audio) {
        this.audio = audio;
    }

    public String getChinese() {
        return chinese;
    }

    public void setChinese(String chinese) {
        this.chinese = chinese;
    }

    public String getNature() {
        return nature;
    }

    public void setNature(String nature) {
        this.nature = nature;
    }

    public String getAntonym() {
        return antonym;
    }

    public void setAntonym(String antonym) {
        this.antonym = antonym;
    }

    public String getHomoionym() {
        return homoionym;
    }

    public void setHomoionym(String homoionym) {
        this.homoionym = homoionym;
    }

    public String getUsedPhrases() {
        return usedPhrases;
    }

    public void setUsedPhrases(String usedPhrases) {
        this.usedPhrases = usedPhrases;
    }
}